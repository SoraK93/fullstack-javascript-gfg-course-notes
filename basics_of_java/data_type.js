// Primitive Data Type

// String
let userName = "Prakash";
console.log(userName);
console.log(typeof userName);


// Number
let age = 99.5;
console.log(typeof age);


// Boolean
let isProductInCart = false;
// if  product is added to cart then isProductInCart = true otherwise isProductInCart = false;
console.log(typeof isProductInCart);

if (isProductInCart) {
    console.log("Product is in the cart.");
} else {
    console.log("Product is not in the cart.");
}

// Undefined
let new_user;
console.log(new_user);
console.log(typeof new_user);


// Null
let noValue = null;
console.log(noValue);
console.log(typeof noValue);


// Non-Primitive Data Type

// Object
// Key-Value Pair
const personDetails = {
    name: "Sourabh",
    age: 32,
    education: "MBA",
};
console.log(typeof personDetails);

const arrayOfNumbers = [1, 2, 3, 4, 5];
console.log(typeof arrayOfNumbers);

